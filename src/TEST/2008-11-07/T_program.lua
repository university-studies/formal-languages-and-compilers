function main()
local Karla;
local Ingrid;
local Klaudie;
    Karla = 6;
    Ingrid = 2.0e-1;
    
    Klaudie = 6 * 2.0e-1;
    write(Klaudie);
    Klaudie = 2.0e-1 * 6;
    write(Klaudie);
    Klaudie = 2.0e-1 * 2.0e-1;
    write(Klaudie);

    Klaudie = Karla * 2.0e-1;
    write(Klaudie);
    Klaudie = 2.0e-1 * Karla;
    write(Klaudie);
    Klaudie = 2.0e-1 * 2.0e-1;
    write(Klaudie);

    Klaudie = 6 * Ingrid;
    write(Klaudie);
    Klaudie = Ingrid * 6;
    write(Klaudie);
    Klaudie = Ingrid * Ingrid;
    write(Klaudie);
    
    Klaudie = Ingrid * 2.0e-1;
    write(Klaudie);
    Klaudie = 2.0e-1 * Ingrid;
    write(Klaudie);
    Klaudie = Ingrid * Ingrid;
    write(Klaudie);
end;
